"""String Manipulation"""

phrase = "Monty Python's flying circus"

print(phrase.upper())
# MONTY PYTHON'S FLYING CIRCUS

print(phrase.lower())
# monty python's flying circus

print(phrase.title())
# Monty Python'S Flying Circus

print(phrase.capitalize())
# Monty python's flying circus

print(phrase.swapcase())
# mONTY pYTHON'S FLYING CIRCUS