"""String Manipulation"""

# check if all char are alphanumeric
print("MontyPythonsFlyingCircus123456789".isalnum())
# True

# check if all char in the string are alphabetic
print("MontyPythonsFlyingCircus".isalpha())
# True

print("1234567890".isdigit())  # check if all char in the string are digits
# True

print("Monty Python".istitle())   # test if string contains title words
# True

# check if all char in the string are upper case
print("MONTY PYTHON".isupper())
# True

# check if all char in the string are lower case
print("monty python".islower())
# True

print("   ".isspace())   # check if all char in the string are spaces
# True
